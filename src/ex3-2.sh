#!/bin/bash

echo "### y = (1/2)x^2 실수 계산기 ###"
echo "계산할 실수 값(x)을 두 개 이상 입력하세요 (예: 2.5 4 6.7): "

# 사용자로부터 여러 개의 값 입력 받기
read -a INPUT_VALUES

# 입력된 값의 개수가 2개 이상인지 확인
if [ ${#INPUT_VALUES[@]} -lt 2 ]; then
    echo "오류: 최소한 두 개 이상의 값을 입력해야 합니다."
    exit 1
fi

echo "-----------------------------------"
echo "입력 X | 출력 Y = 0.5 * X^2"
echo "-----------------------------------"

# 반복문을 사용하여 입력된 각 값에 대해 계산 수행
for x in "${INPUT_VALUES[@]}"; do
    # bc 명령어를 사용하여 실수 계산 수행
    # scale=4: 소수점 4째 자리까지 정확도를 설정
    # (0.5 * x * x)를 계산
    y=$(echo "scale=4; 0.5 * $x * $x" | bc)
    
    # 결과를 출력
    printf " %-6s | %s\n" "$x" "$y"
done
